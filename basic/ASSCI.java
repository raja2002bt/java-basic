package basic;

public class ASSCI {

	public static void main(String[] args) {
		for(int i=0;i<=227;i++) {
			System.out.println(i+" "+(char)i);
		}
		for(int j=65;j<=90;j++) {                          //A-Z
			System.out.println(j+" "+(char)j);
		}
		for(int k=97;k<=122;k++) {                         //a-z
			System.out.println(k+" "+(char)k);
		}
		for(int l=48;l<=57;l++) {                          //0-1
			System.out.println(l+" "+(char)l);
		}
	}

}
